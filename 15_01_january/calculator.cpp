#include <iostream>
using namespace std;

int main() {
    int N;
    int M;

    cin>>M;
    cin>>N;

    cout<<M<<" + "<<N<<" = "<<M+N<<endl;
    cout<<M<<" - "<<N<<" = "<<M-N<<endl;
    cout<<M<<" * "<<N<<" = "<<M*N<<endl;
    cout<<M<<" / "<<N<<" = "<<M/N<<endl;
    cout<<M<<" % "<<N<<" = "<<M%N<<endl;
    return 0;
}

#include<iostream>
using namespace std;

int main(){

    long long M;
    long long N;

    cin>>M;
    cin>>N;

    cout<<M<<" + "<<N<<" = "<<M+N<<endl;
    cout<<M<<" - "<<N<<" = "<<M-N<<endl;
    cout<<M<<" * "<<N<<" = "<<M*N<<endl;
    cout<<M<<" / "<<N<<" = "<<M/N<<endl;
    cout<<M<<" % "<<N<<" = "<<M%N<<endl;
    return 0;
}